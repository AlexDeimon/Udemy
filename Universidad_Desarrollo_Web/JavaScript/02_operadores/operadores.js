//aritmeticos
let a = 6, b = 4;
let z = a + b;
console.log("Resultado de la suma: "+z);

z = a - b;
console.log("Resultado de la resta: "+z);

z = a * b;
console.log("Resultado de la multiplicación: "+z);

z = a / b;
console.log("Resultado de la división: "+z);

z = a % b;
console.log("Resultado del modulo(residuo): "+z);

z = a ** b;
console.log("Resultado del exponente: "+z);

//incremento
//preincremento
z = ++a;
console.log(a);
console.log(z);

//postincremento
z = b++;
console.log(b);
console.log(z);

//decremento
//predecremento
z = --a;
console.log(a);
console.log(z);

//postdecremento
z = b--;
console.log(b);
console.log(z);